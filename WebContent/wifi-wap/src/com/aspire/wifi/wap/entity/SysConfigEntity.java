package com.aspire.wifi.wap.entity;

/**
 * 系统配置信息
 * **/
public class SysConfigEntity {

	private String configParaCode;
	
	private String configParaValue;
	
	private String configParaUnits;
	
	private String configParaDesc;
	
	private String configParaIfactive;

	public String getConfigParaCode() {
		return configParaCode;
	}

	public void setConfigParaCode(String configParaCode) {
		this.configParaCode = configParaCode;
	}

	public String getConfigParaValue() {
		return configParaValue;
	}

	public void setConfigParaValue(String configParaValue) {
		this.configParaValue = configParaValue;
	}

	public String getConfigParaUnits() {
		return configParaUnits;
	}

	public void setConfigParaUnits(String configParaUnits) {
		this.configParaUnits = configParaUnits;
	}

	public String getConfigParaDesc() {
		return configParaDesc;
	}

	public void setConfigParaDesc(String configParaDesc) {
		this.configParaDesc = configParaDesc;
	}

	public String getConfigParaIfactive() {
		return configParaIfactive;
	}

	public void setConfigParaIfactive(String configParaIfactive) {
		this.configParaIfactive = configParaIfactive;
	}
}
