package com.aspire.wifi.wap.entity;

import java.math.BigInteger;

public class PinActionEntity {
	private BigInteger activityId;
	private BigInteger flashSaleId;
	private String ownerMobile;
	private String flashSaleDate;
	private String expiredDate;
	private String createtableDate;

	public BigInteger getFlashSaleId() {
		return flashSaleId;
	}

	public void setFlashSaleId(BigInteger flashSaleId) {
		this.flashSaleId = flashSaleId;
	}

	public String getOwnerMobile() {
		return ownerMobile;
	}

	public void setOwnerMobile(String ownerMobile) {
		this.ownerMobile = ownerMobile;
	}

	public String getFlashSaleDate() {
		return flashSaleDate;
	}

	public void setFlashSaleDate(String flashSaleDate) {
		this.flashSaleDate = flashSaleDate;
	}

	public String getExpiredDate() {
		return expiredDate;
	}

	public void setExpiredDate(String expiredDate) {
		this.expiredDate = expiredDate;
	}

	public String getCreatetableDate() {
		return createtableDate;
	}

	public void setCreatetableDate(String createtableDate) {
		this.createtableDate = createtableDate;
	}

	public BigInteger getActivityId() {
		return activityId;
	}

	public void setActivityId(BigInteger activityId) {
		this.activityId = activityId;
	}

}
